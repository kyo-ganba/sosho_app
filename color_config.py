def load_colors(館: str) -> dict:
    p = DATA_DIR / f"{館}_colors.json"
    if p.exists():
        saved = json.loads(p.read_text(encoding="utf-8"))
        # 新しいキーはデフォルト値で補完
        return {**DEFAULT_COLORS, **saved}
    return dict(DEFAULT_COLORS)


def save_colors(館: str, colors: dict):
    p = DATA_DIR / f"{館}_colors.json"
    p.write_text(json.dumps(colors, ensure_ascii=False, indent=2),
                 encoding="utf-8")


def reset_colors(館: str):
    p = DATA_DIR / f"{館}_colors.json"
    if p.exists():
        p.unlink()
